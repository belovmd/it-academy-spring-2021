# Предположим, что с начала суток прошло N минут.
# Напишите программу, которая по введённому пользователем количеству минут
# N (натуральное число) определит позицию
# стрелок на циферблате: часовой (0 <= H < 12) и минутной (0 <= M < 60).
# Программа должна вывести получившийся момент времени в следующем формате:

N = int(input())

print((N // 60) % 12, ':', N % 60, sep='')
