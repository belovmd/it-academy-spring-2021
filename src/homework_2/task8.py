# n программистов делят k печенек поровну, неделящийся остаток остается в рюкзаке.
# Сколько печенек достанется каждому программисту? Сколько печенек останется в корзинке?
# Программа получает на вход числа n и k и должна вывести искомое количество печенек (два числа).

n = int(input())
k = int(input())

print(k // n)
print(k % n)

